def main():
    elementNumber = int(input())
    if elementNumber == 3:
        print("Li")
    elif elementNumber == 25:
        print("Mn")
    elif elementNumber == 17:
        print("Cl")
    elif elementNumber == 80:
        print("Hg")
    else:
        print("Unknown element")


if __name__ == "__main__":
    main()
